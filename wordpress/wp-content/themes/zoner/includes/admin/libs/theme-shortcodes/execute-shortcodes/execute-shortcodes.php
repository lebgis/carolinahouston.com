	function zoner_select_all_agency_param() {
		global $wp_query, $zoner, $zoner_config;
		$q_agancy = null;
		$args = $all_agnecies = array();
		$args = array(
			'post_type' => 'agency',
			'post_status' => 'publish',
			'posts_per_page' => -1,
			'order'			 => 'ASC',
			'orderby'		 => 'name'
		);
		
		$q_agency = new WP_Query( $args );
		$all_agnecies[__('No agencies')] = -1;

		while ( $q_agency->have_posts() ) {
				$q_agency->the_post();
				
				$title = get_the_title();
				$id_ = get_the_ID();
				$all_agnecies[$title] = $id_;
			
		}
		
		wp_reset_query();
		wp_reset_postdata();
		
		return $all_agnecies;
	}
	
	
